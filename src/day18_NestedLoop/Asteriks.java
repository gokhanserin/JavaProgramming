package day18_NestedLoop;

public class Asteriks {

    public static void main(String[] args) {

        for (int k = 1; k < 11 ; k++) {

            for (int i = 1; i < 11 ; i++) {
                System.out.print("*  ");
            }
            System.out.println();
        }


    }


}
